#Comparison operators give boolean values
print(3>2)
print(3<2)
print(3>=3)
print(3<=2)
print(3!=2)
print(3==2)

#Logical Operators also give boolean result
print (3>2 or 2>3)
print (3>2 or 2<3)
print (3<2 or 2==3)

print (3>2 and 2>3)
print (3>2 and 2<3)
print (3>2 and 3==3)

print(not(2>3))
print(not(3==3))
