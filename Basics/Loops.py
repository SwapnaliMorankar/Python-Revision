#while loop
Number = 1
while(Number<10):
    Number += 2
    print(Number)
print("End")


#star pattern
i=1
while(i<=5):
    print(i * "*")
    i+=1
print("End")


# reverse star pattern
i=5
while(i>=1):
    print(i * "*")
    i-=1
print("End")


#for loop
for i in range(1,10):
    print(i)
print("END")